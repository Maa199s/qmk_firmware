bool oled_task_user(void) {
  if (naginata_state() != ng_state) {
    update_oled = true;
    ng_state = naginata_state();
  }
  if (is_keyboard_master()) {
    if (update_oled) { // 内容が変化する時だけ書き換えないとスリープに入らない。
      if (naginata_state()) {
        render_kana();
      } else {
        render_eisu();
      }
      render_mode();
      update_oled = false;
    }
  } else {
    naginata_logo();
  }
  return false;
}
